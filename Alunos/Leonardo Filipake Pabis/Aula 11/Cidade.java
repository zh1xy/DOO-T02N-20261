public class Cidade {
    String temperatura;
    String maximo;
    String minima;
    String humidade;
    String condicao;
    String quantidadePrecipitacao;
    String velocidadeVento;
    String direcaoVento;

    public Cidade(){

    }


    public String getTemperatura() {
        return temperatura;
    }
    public void setTemperatura(String temperatura) {
        this.temperatura = temperatura;
    }
    public String getMaximo() {
        return maximo;
    }
    public void setMaximo(String maximo) {
        this.maximo = maximo;
    }
    public String getMinima() {
        return minima;
    }
    public void setMinima(String minima) {
        this.minima = minima;
    }
    public String getHumidade() {
        return humidade;
    }
    public void setHumidade(String humidade) {
        this.humidade = humidade;
    }
    public String getCondicao() {
        return condicao;
    }
    public void setCondicao(String condicao) {
        this.condicao = condicao;
    }
    public String getQuantidadePrecipitacao() {
        return quantidadePrecipitacao;
    }
    public void setQuantidadePrecipitacao(String quantidadePrecipitacao) {
        this.quantidadePrecipitacao = quantidadePrecipitacao;
    }
    public String getVelocidadeVento() {
        return velocidadeVento;
    }
    public void setVelocidadeVento(String velocidadeVento) {
        this.velocidadeVento = velocidadeVento;
    }
    public String getDirecaoVento() {
        return direcaoVento;
    }
    public void setDirecaoVento(String direcaoVento) {
        this.direcaoVento = direcaoVento;
    }

    
}